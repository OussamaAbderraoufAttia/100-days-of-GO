package main

import "fmt"

func main() {
	// Declaring variables using different methods
	var age int = 25
	price := 19.99
	name := "Alice"
	isGoFun := true

	// Printing values and their types
	fmt.Printf("Value: %v, Type: %T\n", age, age)
	fmt.Printf("Value: %v, Type: %T\n", price, price)
	fmt.Printf("Value: %v, Type: %T\n", name, name)
	fmt.Printf("Value: %v, Type: %T\n", isGoFun, isGoFun)
}
